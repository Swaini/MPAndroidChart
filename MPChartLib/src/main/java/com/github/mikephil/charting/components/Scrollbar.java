package com.github.mikephil.charting.components;


public class Scrollbar
{
	int scrollHeight;
	int scrollColor;
	int scrollOffSetBottom;
	int radiusCorners;
	
	public int getScrollHeight()
	{
		return scrollHeight;
	}
	
	public void setScrollHeight(int scrollHeight)
	{
		this.scrollHeight = scrollHeight;
	}
	
	public int getScrollColor()
	{
		return scrollColor;
	}
	
	public void setScrollColor(int scrollColor)
	{
		this.scrollColor = scrollColor;
	}
	
	public int getScrollOffSetBottom()
	{
		return scrollOffSetBottom;
	}
	
	public void setScrollOffSetBottom(int scrollOffSetBottom)
	{
		this.scrollOffSetBottom = scrollOffSetBottom;
	}
	
	public int getRadiusCorners()
	{
		return radiusCorners;
	}
	
	public void setRadiusCorners(int radiusCorners)
	{
		this.radiusCorners = radiusCorners;
	}
}
